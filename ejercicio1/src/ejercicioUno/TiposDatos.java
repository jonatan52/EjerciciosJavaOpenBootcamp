package ejercicioUno;

public class TiposDatos {
    public static void main(String[] args) {
        int numeroentero = 5;
        long numeroLong = 1018146504;
        float numeroFlotante = 12.5f;
        double numeroDecimal = 14.56d;
        boolean ValorVerdadero = true;
        boolean ValorFalso = false;
        char letra = 'J';
        String Cadena = "Jonatan";

        System.out.println("Numero Entero = " + numeroentero);
        System.out.println("Numero Long = " + numeroLong);
        System.out.println("numeroFlotante = " + numeroFlotante);
        System.out.println("numero Decimal = " + numeroDecimal);
        System.out.println("ValorVerdadero = " + ValorVerdadero);
        System.out.println("ValorFalso = " + ValorFalso);
        System.out.println("letra = " + letra);
        System.out.println("Cadena = " + Cadena);

    }
}
